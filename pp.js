const url = 'https://www.instagram.com/eventos_e_cia_master/?next=%2F'
const porta = document.querySelector('#porta')

function openInNewTab(url) {
    const nova = window.open(url, '_blank')
    nova.focus()
}

porta.addEventListener('click', ()=>{
    openInNewTab(url)
});

